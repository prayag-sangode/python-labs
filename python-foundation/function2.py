# Lab: Function to add two numbers and return the result

# Define the function
def add_numbers(a, b):
    return a + b

# Call the function and store result
result1 = add_numbers(10, 5)
result2 = add_numbers(7, 3)

# Print the results
print("Sum of 10 and 5 is:", result1)
print("Sum of 7 and 3 is:", result2)
